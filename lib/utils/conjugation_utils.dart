class ConjugationUtils {
  const ConjugationUtils();

  List<ConjugationResult>? getConjugations(Vocab vocab) {
    PartOfSpeech? foundPos = getPartOfSpeech(vocab);

    if (foundPos == null) return null;

    if (foundPos.index <= PartOfSpeech.adjectiveT.index) {
      return _getAdjectiveConjugations(vocab, foundPos);
    } else {
      return _getVerbConjugations(vocab, foundPos);
    }
  }

  List<ConjugationResult> _getAdjectiveConjugations(
    Vocab vocab,
    PartOfSpeech pos,
  ) {
    List<ConjugationResult> list = [];

    // Non-past casual
    list.add(
      ConjugationResult(
        'Non-past',
        _conjugate(vocab, pos, ConjugationForm.nonPast, false, false, 1),
        _conjugate(vocab, pos, ConjugationForm.nonPast, true, false, 1),
      ),
    );

    // Past casual
    list.add(
      ConjugationResult(
        'Past',
        _conjugate(vocab, pos, ConjugationForm.past, false, false, 1),
        _conjugate(vocab, pos, ConjugationForm.past, true, false, 1),
      ),
    );

    return list;
  }

