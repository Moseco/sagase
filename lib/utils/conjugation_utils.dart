  PartOfSpeech? getPartOfSpeech(Vocab vocab) {
    for (var pos in vocab.definitions[0].pos ?? []) {
      if (allowedPos.contains(pos)) {
        return pos;
      }
    }

    return null;
  }

  String _conjugate(
    Vocab vocab,
    PartOfSpeech pos,
    ConjugationForm form,
    bool negative,
    bool formal,
    int onum,
  ) {
    // Get base of the vocab
    late String base;
    if (vocab.kanjiReadingPairs[0].kanjiWritings == null ||
        vocab.isUsuallyKanaAlone()) {
      base = vocab.kanjiReadingPairs[0].readings[0].reading;
    } else {
      base = vocab.kanjiReadingPairs[0].kanjiWritings![0].kanji;
    }

    // na-adjectives and suru verbs can have a base length of 1, so ignore length rule for them
    // and isKana doesn't matter so it can remain true
    bool isKana = true;
    if (pos != PartOfSpeech.adjectiveNa && pos != PartOfSpeech.verbSuru) {
      if (base.length < 2) return '';

      isKana = base.codeUnitAt(base.length - 2) > 'あ'.codeUnitAt(0) &&
          base.codeUnitAt(base.length - 2) <= 'ん'.codeUnitAt(0);
    }

    // Try to get conjugation for the current vocab and desired conjugation
    Conjugation? conjugation = conjugationMap[
        '${pos.index.toString().padLeft(2, '0')}${form.index.toString().padLeft(2, '0')}$negative$formal$onum'];
    if (conjugation == null) return '';

    final buffer = StringBuffer();

    if (isKana && conjugation.euphr != null) {
      buffer.write(base.substring(0, base.length - conjugation.stem - 1));
      buffer.write(conjugation.euphr);
    } else if (!isKana && conjugation.euphk != null) {
      buffer.write(base.substring(0, base.length - conjugation.stem - 1));
      buffer.write(conjugation.euphk);
    } else {
      buffer.write(base.substring(0, base.length - conjugation.stem));
    }

    buffer.write(conjugation.okuri);

    return buffer.toString();
  }
}

